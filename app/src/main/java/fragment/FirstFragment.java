package fragment;

import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.text.Editable;
import android.text.TextWatcher;
import android.util.Log;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.EditText;
import android.widget.TextView;
import android.widget.Toast;

import com.example.puttipong.testfragment.R;

public class FirstFragment extends Fragment {

    EditText etFirst;
    SecondFragment fragment;

    public FirstFragment() {
        super();
    }

    public static FirstFragment newInstance() {
        FirstFragment fragment = new FirstFragment();
        Bundle args = new Bundle();
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View rootView = inflater.inflate(R.layout.fragment_first, container, false);
        initInstances(rootView);
        return rootView;
    }

    private void initInstances(View rootView) {
        // Init 'View' instance(s) with rootView.findViewById here
        etFirst = (EditText) rootView.findViewById(R.id.etFirst);

        etFirst.addTextChangedListener(new MyTextWatcher(etFirst));
    }

    public void setChangedText(String txt) {
        Toast.makeText(getContext(),
                "FirstFragment was changed: \n" + txt,
                Toast.LENGTH_SHORT).show();
        etFirst.setText(txt);
    }

    @Override
    public void onStart() {
        super.onStart();
    }

    @Override
    public void onStop() {
        super.onStop();
    }

    /*
     * Save Instance State Here
     */
    @Override
    public void onSaveInstanceState(Bundle outState) {
        super.onSaveInstanceState(outState);
        // Save Instance State here
    }

    /*
     * Restore Instance State Here
     */
    @Override
    public void onActivityCreated(Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);
        if (savedInstanceState != null) {
            // Restore Instance State here
        }
    }

    private class MyTextWatcher implements TextWatcher {
        private View view;

        private MyTextWatcher(View view) {
            this.view = view;
        }

        @Override
        public void beforeTextChanged(CharSequence s, int start, int count, int after) {
            fragment = (SecondFragment) getFragmentManager()
                    .findFragmentById(R.id.contentContainerLower);
        }

        @Override
        public void onTextChanged(CharSequence s, int start, int before, int count) {

        }

        @Override
        public void afterTextChanged(Editable editable) {
            etFirst.removeTextChangedListener(new MyTextWatcher(etFirst));
            String text = editable.toString();
            if (etFirst.isFocused()) {
                fragment.setChangedText(text);
            }
        }
    }
}